    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usuario = txtUsuario.getText();
        String password = new String(txtContraseña.getPassword());

        Usuario u = UsuarioLogico.validarUsuario(usuario, password);

        if (u != null) {
            JOptionPane.showMessageDialog(this, "Bienvenido " + u.getUsuario());

            // Redirigir según rol
            if (u.getRol().equals("docente")) {
                FrmDocente f = new FrmDocente();
                f.setVisible(true);
            } else if (u.getRol().equals("alumno")) {
                FrmAlumno f = new FrmAlumno();
                f.setVisible(true);
            }
            this.dispose(); // Cierra login
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed


